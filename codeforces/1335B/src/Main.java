import java.util.Scanner;

public class Main {

    int n, a, b;

    void permutation(char[] chs, int cur) {
        if (cur == n) {
            return;
        }
    }

    void solve() {
        Scanner in = new Scanner(System.in);
        int t = in.nextInt();
        while (t > 0) {
            n = in.nextInt();
            a = in.nextInt();
            b = in.nextInt();
            t--;
        }
    }

    public static void main(String[] args) {
        new Main().solve();
    }

}
